public class FreeCmVO {
	private String fcNm;
	private String fcContent;
	private String fcDate;
	private String fcType;
	private String freeNm;
	private String fcWriter;
	
	public String getFcNm() {
		return fcNm;
	}
	public void setFcNm(String fcNm) {
		this.fcNm = fcNm;
	}
	public String getFcContent() {
		return fcContent;
	}
	public void setFcContent(String fcContent) {
		this.fcContent = fcContent;
	}
	public String getFcDate() {
		return fcDate;
	}
	public void setFcDate(String fcDate) {
		this.fcDate = fcDate;
	}
	public String getFcType() {
		return fcType;
	}
	public void setFcType(String fcType) {
		this.fcType = fcType;
	}
	public String getFreeNm() {
		return freeNm;
	}
	public void setFreeNm(String freeNm) {
		this.freeNm = freeNm;
	}
	public String getFcWriter() {
		return fcWriter;
	}
	public void setFcWriter(String fcWriter) {
		this.fcWriter = fcWriter;
	}
	
	
}
