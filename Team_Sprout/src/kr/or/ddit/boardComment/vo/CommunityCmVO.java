public class CommunityCmVO {
	private String ccNm;
	private String ccContent;
	private String ccDate;
	private String ccType;
	private String cbNm;
	private String ccWriter;
	
	public String getCcNm() {
		return ccNm;
	}
	public void setCcNm(String ccNm) {
		this.ccNm = ccNm;
	}
	public String getCcContent() {
		return ccContent;
	}
	public void setCcContent(String ccContent) {
		this.ccContent = ccContent;
	}
	public String getCcDate() {
		return ccDate;
	}
	public void setCcDate(String ccDate) {
		this.ccDate = ccDate;
	}
	public String getCcType() {
		return ccType;
	}
	public void setCcType(String ccType) {
		this.ccType = ccType;
	}
	public String getCbNm() {
		return cbNm;
	}
	public void setCbNm(String cbNm) {
		this.cbNm = cbNm;
	}
	public String getCcWriter() {
		return ccWriter;
	}
	public void setCcWriter(String ccWriter) {
		this.ccWriter = ccWriter;
	}
	
	
	
	
}
