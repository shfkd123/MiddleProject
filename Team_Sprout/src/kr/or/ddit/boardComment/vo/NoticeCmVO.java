package kr.or.ddit.boardComment.vo;

public class NoticeCmVO {
	private String ncNm;
	private String ncContent;
	private String ncDate;
	private String ncType;
	private String noticeNm;
	private String userId;
	
	public String getNcNm() {
		return ncNm;
	}
	public void setNcNm(String ncNm) {
		this.ncNm = ncNm;
	}
	public String getNcContent() {
		return ncContent;
	}
	public void setNcContent(String ncContent) {
		this.ncContent = ncContent;
	}
	public String getNcDate() {
		return ncDate;
	}
	public void setNcDate(String ncDate) {
		this.ncDate = ncDate;
	}
	public String getNcType() {
		return ncType;
	}
	public void setNcType(String ncType) {
		this.ncType = ncType;
	}
	public String getNoticeNm() {
		return noticeNm;
	}
	public void setNoticeNm(String noticeNm) {
		this.noticeNm = noticeNm;
	}
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	
	

}
